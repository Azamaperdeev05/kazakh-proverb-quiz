const answers = proverbPairs.map(([, answer]) => answer)

function buildOptions(answer: string, index: number) {
  const picked = new Set<string>([answer])
  const options = [answer]

  let cursor = index + 1
  while (options.length < 4) {
    const candidate = answers[cursor % answers.length]
    if (!picked.has(candidate)) {
      picked.add(candidate)
      options.push(candidate)
    }
    cursor += 7
  }

  return options.sort((a, b) => a.localeCompare(b, 'kk'))
}

export const PROVERBS: ProverbItem[] = proverbPairs.map(([start, answer], index) => ({
  id: `maqal-${index + 1}`,
  start,
  answer,
  options: buildOptions(answer, index),
}))

export const TOTAL_PROVERBS = PROVERBS.length
